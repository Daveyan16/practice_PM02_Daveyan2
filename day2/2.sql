-- 1. ТАБЛИЦА: Категории
-- =====================================================
CREATE TABLE Категории (
    id_категории INT NOT NULL AUTO_INCREMENT,
    название VARCHAR(50) NOT NULL,
    PRIMARY KEY (id_категории)
);

-- =====================================================
-- 2. ТАБЛИЦА: Покупатели
-- =====================================================
CREATE TABLE Покупатели (
    id_покупателя INT NOT NULL AUTO_INCREMENT,
    фио VARCHAR(100) NOT NULL,
    email VARCHAR(100) NOT NULL,
    телефон VARCHAR(20),
    адрес VARCHAR(200),
    PRIMARY KEY (id_покупателя)
);

-- =====================================================
-- 3. ТАБЛИЦА: Товары
-- =====================================================
CREATE TABLE Товары (
    id_товара INT NOT NULL AUTO_INCREMENT,
    название VARCHAR(100) NOT NULL,
    цена DECIMAL(10,2) NOT NULL,
    описание TEXT,
    количество_на_складе INT NOT NULL DEFAULT 0,
    id_категории INT NOT NULL,
    PRIMARY KEY (id_товара),
    FOREIGN KEY (id_категории) REFERENCES Категории(id_категории)
);

-- =====================================================
-- 4. ТАБЛИЦА: Заказы
-- =====================================================
CREATE TABLE Заказы (
    id_заказа INT NOT NULL AUTO_INCREMENT,
    дата_заказа DATE NOT NULL,
    id_покупателя INT NOT NULL,
    статус VARCHAR(50) DEFAULT 'новый',
    PRIMARY KEY (id_заказа),
    FOREIGN KEY (id_покупателя) REFERENCES Покупатели(id_покупателя)
);

-- =====================================================
-- 5. ТАБЛИЦА: Состав_заказа (промежуточная)
-- =====================================================
CREATE TABLE Состав_заказа (
    id_заказа INT NOT NULL,
    id_товара INT NOT NULL,
    количество INT NOT NULL DEFAULT 1,
    цена_на_момент_заказа DECIMAL(10,2) NOT NULL,
    PRIMARY KEY (id_заказа, id_товара),
    FOREIGN KEY (id_заказа) REFERENCES Заказы(id_заказа),
    FOREIGN KEY (id_товара) REFERENCES Товары(id_товара)
);

-- =====================================================
-- 6. ТАБЛИЦА: Отзывы
-- =====================================================
CREATE TABLE Отзывы (
    id_отзыва INT NOT NULL AUTO_INCREMENT,
    id_товара INT NOT NULL,
    id_покупателя INT NOT NULL,
    оценка INT NOT NULL,
    текст TEXT,
    дата DATE NOT NULL,
    PRIMARY KEY (id_отзыва),
    FOREIGN KEY (id_товара) REFERENCES Товары(id_товара),
    FOREIGN KEY (id_покупателя) REFERENCES Покупатели(id_покупателя)
);

